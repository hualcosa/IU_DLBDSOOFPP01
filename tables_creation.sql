    id SERIAL PRIMARY KEY,
    name VARCHAR(255),
    creation_date DATETIME,
    periodicity VARCHAR(255)
);

CREATE TABLE habits_history (
    id INTEGER NOT NULL,
    completion_date DATETIME
);

